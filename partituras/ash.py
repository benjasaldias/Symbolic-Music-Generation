import numpy as np

def matrix_to_lilypond(matrix):
    # Inicializar una lista para almacenar las líneas de LilyPond
    lilypond_lines = []
    
    # Definir las notas correspondientes a los índices (ajustar según tu necesidad)
    notes = [
        'c,', 'cis,', 'des,', 'd,', 'dis,', 'ees,', 'e,', 'eis,',
        "fes,", 'f,', 'fis,', 'ges,', 'g,', 'gis,', 'aes,', 'a,', 
        'ais,', 'bes,', 'b,', 'bis,', 'ces,',
        'c', 'cis', 'des', 'd', 'dis', 'ees', 'e', 'eis',
        "fes", 'f', 'fis', 'ges', 'g', 'gis', 'aes', 'a', 
        'ais', 'bes', 'b', 'bis', 'ces',
        "c'", "cis'", "des'", "d'", "dis'", "ees'", "e'", "eis'",
        "fes'", "f'", "fis'", "ges'", "g'", "gis'", "aes'", "a'", 
        "ais'", "bes'", "b'", "bis'", "ces'",
        "c''", "cis''", "des''", "d''", "dis''", "ees''", "e''", "eis''",
        "fes''", "f''", "fis''", "ges''", "g''", "gis''", "aes''", "a''", 
        "ais''", "bes''", "b''", "bis''", "ces''", 
        "c'''", "cis'''", "des'''", "d'''", "dis'''", "ees'''", "e'''", "eis'''",
        "fes'''", "f'''", "fis'''", "ges'''", "g'''", "gis'''", "aes'''", "a'''",
        "ais'''", "bes'''", "b'''", "bis'''", "ces'''", "c''''"
        ]
    # Recorrer la matriz y construir el texto LilyPond
    temp_group = []
    line = []
    for row in range(len(matrix)):
        if matrix[row][0] == 1 or matrix[row][21] == 1 or matrix[row][42] == 1 or matrix[row][63] == 1 or matrix[row][84] or matrix[row][105] == 1:
            if temp_group:
                line.append(f"[ {' '.join(temp_group)} ]")
                lilypond_lines.append(' '.join(line))
                line = []
            if matrix[row][0] == 1:
                line.append("c,")
            if matrix[row][21] == 1:
                line.append("c")
            if matrix[row][42] == 1:
                line.append("c'")
            if matrix[row][63] == 1:
                line.append("c''")
            if matrix[row][84] == 1:
                line.append("c'''")
            if matrix[row][105] == 1:
                line.append("c''''")
            temp_group = []
        if row == len(matrix)-1:
            lilypond_lines.append(' '.join(line))
         # Almacenar notas antes de un do
        for i, value in enumerate(matrix[row]):
            if value == 1:
                # Asumiendo que el índice i corresponde a la nota
                note_index = i % len(notes)  # Asegurarse de que el índice no exceda la longitud de la lista de notas
                note = notes[note_index]

                # Verificar si la nota es un do
                if note.startswith('c'):
                    if temp_group:
                        # Si hay un grupo de notas antes del do, agrégalo
                        line.append(f"[ {' '.join(temp_group)} ]")
                else:
                    temp_group.append(note)  # Agregar la nota al grupo temporal

        # Al final de la fila, agregar cualquier grupo restante
        if matrix[row][0] == 1 or matrix[row][21] == 1 or matrix[row][42] == 1 or matrix[row][63] == 1 or matrix[row][84] == 1:
            if temp_group:
                line.append(f"[ {' '.join(temp_group)} ]")

        # Convertir la línea de notas a una cadena y agregarla a las líneas de LilyPond
        # if line and len(line) != 1:
        #     lilypond_lines.append(' '.join(line))
        #     line = []

    # Construir el texto completo de LilyPond
    lilypond_text = "\\relative c' {\n" + '\n'.join(lilypond_lines) + "\n}"
    lilypond_text = '\n'.join(lilypond_lines)
    return lilypond_text

